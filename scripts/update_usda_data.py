def main():
    db_path = "/Users/a39/Documents/AIProject/EmptyRhythm/EmptyRhythm/Resources/foods_database.json"
    with open(db_path) as f:
        foods = json.load(f)

    id_index = {food["id"]: i for i, food in enumerate(foods)}
    updated_ids = set()
    updated_count = 0

    for prefix, (cal, protein, carb, fat, fiber, fdc_id) in USDA_DATA.items():
        matched = 0
        for food_id, idx in id_index.items():
            if food_id == prefix or food_id.startswith(prefix + "_"):
                food = foods[idx]
                food["caloriesPer100g"] = cal
                food["protein"]  = protein
                food["carb"]     = carb
                food["fat"]      = fat
                food["fiber"]    = fiber
                food["dataSource"]  = "USDA_SR_Legacy"
                food["fdcId"]       = fdc_id
                food["confidence"]  = "verified"
                updated_ids.add(food_id)
                matched += 1
        updated_count += matched

    # Q3-B: 给未更新的食物加 estimated 标签
    estimated_count = 0
    for food in foods:
        if "confidence" not in food:
            food["confidence"] = "estimated"
            food["dataSource"] = "estimated"
            estimated_count += 1

    print(f"USDA 覆盖基础食物: {len(USDA_DATA)} 种")
    print(f"更新条目（含变体）: {updated_count}")
    print(f"标记为 estimated: {estimated_count}")
    print(f"总条数: {len(foods)}")

    verified = sum(1 for f in foods if f.get("confidence") == "verified")
    estimated = sum(1 for f in foods if f.get("confidence") == "estimated")
    print(f"\n数据质量统计:")
    print(f"  verified (USDA官方): {verified} 条 ({verified/len(foods)*100:.1f}%)")
    print(f"  estimated (估算):    {estimated} 条 ({estimated/len(foods)*100:.1f}%)")

    with open(db_path, "w", encoding="utf-8") as f:
        json.dump(foods, f, ensure_ascii=False, separators=(",", ":"))

    import os
    size = os.path.getsize(db_path) / 1024 / 1024
    print(f"\n已保存: {db_path} ({size:.1f} MB)")

if __name__ == "__main__":
    main()
